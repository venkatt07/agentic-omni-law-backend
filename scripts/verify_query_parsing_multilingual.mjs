async function ask(q) {
  const rl = readline.createInterface({ input, output });
  try { return (await rl.question(q)).trim(); } finally { rl.close(); }
}
async function api(path, method = "GET", token, body) {
  const res = await fetch(`${apiBase}${path}`, {
    method,
    headers: { ...(token ? { Authorization: `Bearer ${token}` } : {}), ...(body ? { "content-type": "application/json" } : {}) },
    body: body ? JSON.stringify(body) : undefined,
  });
  const txt = await res.text();
  let json = null; try { json = JSON.parse(txt); } catch {}
  if (!res.ok) throw new Error(`${method} ${path} -> ${res.status} ${txt}`);
  return json;
}
function assert(cond, msg) { if (!cond) throw new Error(msg); }
function deduped(citations = []) {
  const seen = new Set();
  for (const c of citations) {
    const norm = String(c.snippet || "").toLowerCase().replace(/\s+/g, " ").trim().slice(0, 140);
    const key = norm ? `${c.source_type || ""}::${c.doc_id || ""}::${norm}` : `${c.source_type || ""}::${c.doc_id || ""}::${c.chunk_id || ""}`;
    if (seen.has(key)) return false;
    seen.add(key);
  }
  return true;
}

async function runCase(token, sample, caseId) {
  await api(`/cases/${caseId}/text`, "POST", token, { text: sample.text, title: "query-context" });
  const started = await api(`/cases/${caseId}/run-all`, "POST", token, { filtersApplied: sample.filters || { jurisdiction: "All India", legal_domain: "All Domains", source_types: ["Acts & Statutes", "Case Laws"] } });
  for (let i = 0; i < 220; i += 1) {
    const status = await api(`/runs/${started.run_id}/status`, "GET", token);
    if (status.status === "SUCCEEDED") break;
    if (status.status === "FAILED") throw new Error(`Run failed for ${sample.name}`);
    await sleep(1000);
  }
  const details = await api(`/cases/${caseId}`, "GET", token);
  const qp = details.outputs?.query_parsing;
  assert(qp, `${sample.name}: missing query_parsing`);
  const dl = typeof qp.detected_language === "object" ? qp.detected_language : { code: "", confidence: 0 };
  const domain = String(qp.legal_domain || qp.domain || "");
  const summary = String(qp.executive_summary_text || qp.summary || "");
  const issueGroups = Array.isArray(qp.issue_groups) ? qp.issue_groups : [];
  const citations = Array.isArray(qp.citations) ? qp.citations : [];
  const filtersHash = JSON.stringify(sample.filters || { jurisdiction: "All India", legal_domain: "All Domains", source_types: ["Acts & Statutes", "Case Laws"] });
  const expectedInputHash = sha256(`${sample.text}::${filtersHash}`);
  assert(dl.code === sample.expectedLang, `${sample.name}: detected lang ${dl.code} != ${sample.expectedLang}`);
  assert(Number(dl.confidence || 0) >= 0.85 || sample.name.includes("Urdu"), `${sample.name}: low detection confidence ${dl.confidence}`);
  assert(sample.expectedDomain.test(domain), `${sample.name}: domain '${domain}' not matched`);
  assert(summary.length > 60, `${sample.name}: summary too short`);
  assert(issueGroups.length >= 3 || /Urdu/.test(sample.name), `${sample.name}: expected >=3 issue groups`);
  assert(deduped(citations), `${sample.name}: duplicate citations found`);
  assert(String(qp.input_hash || "") === expectedInputHash, `${sample.name}: input_hash mismatch`);
  return { sample: sample.name, lang: dl, domain, summary_len: summary.length, issue_groups: issueGroups.length, citations: citations.length, input_hash: qp.input_hash };
}

